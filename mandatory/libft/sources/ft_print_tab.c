#include "../libft.h"

void	print_tab(char **tab)
{
	int	i;
	int	j;

	i = -1;
	while (tab[++i])
	{
		j = -1;
		while (tab[i][++j])
			printf("%c", tab[i][j]);
		printf("\n");
	}
}
